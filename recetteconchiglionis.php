            <h2>préparation</h2>
            <p>Dans une casserole, plongez les épinards et ajoutez de l’eau afin de les recouvrir. Une fois l’eau portée à ébullition, laissez-les mijoter 3 à 5 minutes. Une fois cuits, passez les les épinards sous l’eau froide et égouttez-les en les pressant avec vos mains.</p>
            <p>Dans une autre casserole, portez de l’eau salée à ébullition pour faire  les pâtes. Une fois les conchiglionis cuits et égouttés, laissez refroidir.</p>
            <p>Assaisonner la ricotta avec du sel, du poivre et de la noix de muscade. Ajoutez l’œuf et les épinards cuits. Mélangez pour homogénéiser le tout.</p>
